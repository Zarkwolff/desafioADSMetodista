    public void lerTabela(){
        DefaultTableModel modelo = (DefaultTableModel) tbMoradores.getModel();
        daoMorador dao = new daoMorador();
        modelo.setNumRows(0);
        
        for(Morador m: dao.listar()){
            modelo.addRow(new Object[]{
                m.getIdMorador(),
                m.getNome(),
                m.getCpf(),
                m.getEndereco(),
                m.getCidade(),
                m.getTelefone(),
                m.getEmail()
            });
        }
        
        
    }

